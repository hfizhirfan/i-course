// Menambahkan event listener untuk menangani perubahan tab dan scroll otomatis



function changeTab(element, sectionId) {
    // Dapatkan semua tombol dengan tab yang sama di kedua navigator
    const allTabs = document.querySelectorAll(`.btn-tab`);

    // Ambil indeks tab yang dipilih
    const index = Array.from(element.parentElement.parentElement.children).indexOf(element.parentElement);

    // Hapus class 'active' dari semua tab di kedua navigator
    allTabs.forEach((tab) => {
        tab.classList.remove('active');
    });

    // Tambahkan class 'active' ke tab yang sesuai di kedua navigator
    document.querySelectorAll(`.nav-tabs .nav-item:nth-child(${index + 1}) .btn-tab`).forEach((tab) => {
        tab.classList.add('active');
    });

    // Scroll ke bagian yang sesuai
    scrollToSection(sectionId);
}

function scrollToSection(sectionId) {
    const section = document.getElementById(sectionId);
    if (section) {
        const offset = -165; // Sesuaikan offset sesuai kebutuhan
        const scrollPosition = section.getBoundingClientRect().top + window.pageYOffset + offset;
        window.scrollTo({
            top: scrollPosition,
            behavior: 'smooth'
        });
    }
}

window.addEventListener('scroll', () => {
    const navbar = document.getElementById('tabContainer');
    const triggerPoint = 400; // Ganti dengan posisi scroll yang diinginkan

    if (window.scrollY > triggerPoint) {
        navbar.classList.add('visible');
    } else {
        navbar.classList.remove('visible');
    }
});
function updateActiveTabOnScroll() {
    const sections = [
        { id: 'section1', tab: document.querySelector(`.btn-tab[onclick*="section1"]`) },
        { id: 'section2', tab: document.querySelector(`.btn-tab[onclick*="section2"]`) },
        { id: 'section3', tab: document.querySelector(`.btn-tab[onclick*="section3"]`) },
        { id: 'section4', tab: document.querySelector(`.btn-tab[onclick*="section4"]`) }
    ];

    const offset = 165; // Sesuaikan dengan offset yang digunakan saat scrollToSection

    let activeTab = null;

    sections.forEach(({ id, tab }) => {
        const section = document.getElementById(id);
        if (section) {
            const rect = section.getBoundingClientRect();
            if (rect.top - offset <= 0 && rect.bottom - offset > 0) {
                activeTab = tab;
            }
        }
    });

    // Perbarui status active pada tab
    document.querySelectorAll('.btn-tab').forEach((tab) => {
        tab.classList.remove('active');
    });

    if (activeTab) {
        activeTab.classList.add('active');
    }
}

// Tambahkan event listener pada scroll
window.addEventListener('scroll', updateActiveTabOnScroll);


document.addEventListener('DOMContentLoaded', () => {
    // Setel tab pertama sebagai aktif saat halaman dimuat
    const firstTab = document.querySelector('.nav-item .btn-tab');
    if (firstTab) {
        firstTab.classList.add('active');
    }

    // Menambahkan event listener pada klik tab
    const allTabs = document.querySelectorAll('.btn-tab');
    allTabs.forEach((tab) => {
        tab.addEventListener('click', function () {
            changeTab(this, this.getAttribute('onclick').split(',')[1].replace(/'/g, ''));
        });
    });
});

function changeTab(element, sectionId) {
    // Hapus class 'active' dari semua tab
    const allTabs = document.querySelectorAll('.btn-tab');
    allTabs.forEach((tab) => {
        tab.classList.remove('active');
    });

    // Tambahkan class 'active' ke tab yang dipilih
    element.classList.add('active');

    // Scroll ke bagian yang sesuai
    scrollToSection(sectionId);
}

function scrollToSection(sectionId) {
    const section = document.getElementById(sectionId);
    if (section) {
        const offset = -165; // Sesuaikan offset sesuai kebutuhan
        const scrollPosition = section.getBoundingClientRect().top + window.pageYOffset + offset;
        window.scrollTo({
            top: scrollPosition,
            behavior: 'smooth'
        });
    }
}




